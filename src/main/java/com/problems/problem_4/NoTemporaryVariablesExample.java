package com.problems.problem_4;

public class NoTemporaryVariablesExample {
    public static void main(String[] args) {
        int length = 5;
        int width = 10;

        System.out.println("Площадь: " + (length * width));
        System.out.println("Периметр: " + (2 * (length + width)));
    }
}
